function cambiarIdioma(selectValue)
{
	switch(selectValue){
		case "1": window.location.href = "http://www.hotelmilenio.com";
				break;
		case "2": window.location.href = "http://www.hotelmilenio.fr";
				break;
		case "3": window.location.href = "http://www.hotelmilenio.co.uk";
				break;
		case "4": window.location.href = "http://www.hotelmilenio.ru";
				break;
		default:break;
	}
}